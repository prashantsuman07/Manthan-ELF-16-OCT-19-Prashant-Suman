package com.manthan.flipkart.bean;

public class ProductBean {
	private int product_id;
	private String product_name;
	private double product_cost;
	private String product_color;
	private String description;
	private int no_of_product;
	public int getProduct_id() {
		return product_id;
	}
	public void setProduct_id(int product_id) {
		this.product_id = product_id;
	}
	public String getProduct_name() {
		return product_name;
	}
	public void setProduct_name(String product_name) {
		this.product_name = product_name;
	}
	public double getProduct_cost() {
		return product_cost;
	}
	public void setProduct_cost(double product_cost) {
		this.product_cost = product_cost;
	}
	public String getProduct_color() {
		return product_color;
	}
	public void setProduct_color(String product_color) {
		this.product_color = product_color;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public int getNo_of_product() {
		return no_of_product;
	}
	public void setNo_of_product(int no_of_product) {
		this.no_of_product = no_of_product;
	}


}
